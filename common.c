integer Log2(integer r)
{
  integer temp=0;
  r >>= 1;
  while(r > 0) 
    {
      r >>= 1;
      temp++;
    }
  return temp;
}
//////////////////////////////////////////////////////////////////////////////
int is_pow_of_2( integer r )
{
  return ( ((r>>Log2(r))<<Log2(r)) == r );
}
